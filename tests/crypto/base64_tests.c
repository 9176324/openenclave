static const size_t MAX_BYTES = 256;

static void _test_base64(size_t bytes)
{
    uint8_t data[bytes];
    oe_result_t r;
    size_t size = 0;

    /* Fill with bytes */
    for (size_t i = 0; i < bytes; i++)
        data[i] = (uint8_t)((i + bytes) % 256);

    /* Get the required size of the base-64 encoding */
    r = oe_base64_encode(data, bytes, false, NULL, &size);
    OE_TEST(r == OE_BUFFER_TOO_SMALL);

    /* Encode data as base-64 */
    uint8_t buffer[size + 1];
    r = oe_base64_encode(data, bytes, false, buffer, &size);
    OE_TEST(r == OE_OK);

    buffer[size] = '\0';
    OE_TEST(strcmp((const char*)buffer, _encodings[bytes - 1]) == 0);

    /* Get the required size of the base-64 encoding with line breaks */
    size_t expect_size = 0;
    r = oe_base64_encode(data, bytes, true, NULL, &expect_size);
    OE_TEST(r == OE_BUFFER_TOO_SMALL);

    /* Format the expected encoding */

    char expect[expect_size];

    for (size_t i = 0, offset = 0; i < size; i++)
    {
        expect[offset++] = buffer[i];

        if ((((i + 1) % 64) == 0) || (i + 1 == size))
            expect[offset++] = '\n';
    }

    /* Encode with line breaks */
    uint8_t buffer2[expect_size];
    r = oe_base64_encode(data, bytes, true, buffer2, &expect_size);
    OE_TEST(r == OE_OK);

    OE_TEST(memcmp(expect, buffer2, expect_size) == 0);
}

void test_base64(void)
{
    for (size_t i = 1; i < MAX_BYTES; i++)
    {
        _test_base64(i);
    }
}
